package com.shlomi.golemland.game.Core.Constants;

/**
 * This is core of tiles. It contains char parameters that represent a tile type. <br>
 * When editing map text, then the user should take the chars from here.
 * @author Shlomi
 *
 */
public abstract interface ITileChars {

	/**
	 * 't'
	 */
	public final char CHAR_TNT = 't';

	/**
	 * '?'
	 */
	public final char CHAR_PLAYER = '?';
	/**
	 * '!'
	 */
	public final char CHAR_WALL = '!';
	/**
	 * 'x'
	 */
	public final char CHAR_BRICK = 'x';
	/**
	 * 'c'
	 */
	public final char CHAR_CREEPER = 'c';
	/**
	 * '@'
	 */
	public final char CHAR_CAKE = '@';
	/**
	 * '*'
	 */
	public final char CHAR_TELEPORT = '*';
	/**
	 * ' '
	 */
	public final char CHAR_SIDEWALK = ' ';

	/**
	 * 'z'
	 */
	public final char CHAR_ZOMBIE = 'z';

	/**
	 * 's'
	 */
	public final char CHAR_SPIDER = 's';

	/**
	 * 'y'
	 */
	public final char CHAR_SKELETON = 'y';

}// interface

