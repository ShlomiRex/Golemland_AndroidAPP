package com.shlomi.golemland.game.Core.Constants;

/**
 * Direction interface with chars the represents the direction.
 * @author Shlomi
 *
 */
public interface IDirection {
	/**
	 * Oops! (No direction)
	 */
	public final char DIRECTION_OOPS = 'o';

	/**
	 * UP
	 */
	public final char DIRECTION_UP = 'u';

	/**
	 * DOWN
	 */
	public final char DIRECTION_DOWN = 'd';

	/**
	 * LEFT
	 */
	public final char DIRECTION_LEFT = 'l';

	/**
	 * RIGHT
	 */
	public final char DIRECTION_RIGHT = 'r';

}
